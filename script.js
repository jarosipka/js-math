function mathOperations(x, y) {
  console.log("Zadané parametre: " + x + ", " + y);
  console.log("Vykonávam matematické operácie  (+ - * /)");
  console.log(x + "+" + y + "=" + (x + y));
  console.log(x + "-" + y + "=" + (x - y));
  console.log(x + "*" + y + "=" + x * y);
  console.log(x + "/" + y + "=" + x / y);
}

mathOperations(0, 0);
mathOperations(4, 2);
